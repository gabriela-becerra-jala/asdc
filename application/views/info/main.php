                <div class="content">

                    El <b>Síndrome de Down</b> es una combinación cromosómica natural que siempre ha formado parte de la condición humana, existe en todas las regiones del mundo y habitualmente tiene efectos variables en los estilos de aprendizaje, las características físicas o la salud.
                    <br>
                    <br> El acceso adecuado a la atención de la salud, a los programas de intervención temprana y a la enseñanza inclusiva, así como la investigación adecuada, son vitales para el crecimiento y el desarrollo de la persona.
                    <br>
                    <br> En diciembre de 2011, la Asamblea General designó el 21 de marzo Día Mundial del Síndrome de Down (A/RES/66/149).
                    <br> Con esta celebración, la Asamblea General quiere aumentar la conciencia pública sobre la cuestión y recordar la dignidad inherente, la valía y las valiosas contribuciones de las personas con discapacidad intelectual como promotores del bienestar y de la diversidad de sus comunidades. También quiere resaltar la importancia de su autonomía e independencia individual, en particular la libertad de tomar sus propias decisiones.
